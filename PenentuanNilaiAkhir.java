    private void btnProsesActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnProsesActionPerformed
        double tugas = Double.parseDouble(tugasField.getText());
        double uts = Double.parseDouble(utsField.getText());
        double uas = Double.parseDouble(uasField.getText());

        double akhir = (tugas * 0.4) + (uts * 0.3) + (uas * 0.3);
        akhirField.setText(Double.toString(akhir));

        if (akhir >= 80) {
            gradeField.setText("A");
            keteranganField.setText("Lulus");
        } else if (akhir >= 70) {
            gradeField.setText("B");
            keteranganField.setText("Lulus");
        } else if (akhir >= 56) {
            gradeField.setText("C");
            keteranganField.setText("Lulus");
        } else if (akhir >= 40) {
            gradeField.setText("D");
            keteranganField.setText("Tidak Lulus");
        } else {
            gradeField.setText("E");
            keteranganField.setText("Tidak Lulus");
        }
    }//GEN-LAST:event_btnProsesActionPerformed

