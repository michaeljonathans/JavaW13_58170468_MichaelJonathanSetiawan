    private void tugasFieldKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_tugasFieldKeyTyped
        char a = evt.getKeyChar();

        if (!((Character.isDigit(a)
                || (a == KeyEvent.VK_BACK_SPACE)
                || (a == KeyEvent.VK_DELETE)))) {
            getToolkit().beep();
            JOptionPane.showMessageDialog(null, "Masukkan hanya angka 0-9");
            evt.consume();
        }
    }//GEN-LAST:event_tugasFieldKeyTyped

